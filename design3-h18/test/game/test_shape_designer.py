import unittest
import numpy as np

from atlas.game.shape_designer import ShapeDesigner
from atlas.game.board_elements import Obstacle
from atlas.game.Cube import Cube, CubeColour
from atlas.game.CubeStorage import CubeStorage
from atlas.vision.util import WorldCoordinate


class ShapeDesignerTest(unittest.TestCase):

    @staticmethod
    def create_canvas(width: int):
        canvas = np.zeros((width, width))
        shape_designer = ShapeDesigner(canvas)
        return canvas, shape_designer

    def test_givenCanvas_whenGeneratingCircleOfRadius6_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(13)
        some_obstacle = Obstacle(WorldCoordinate(12/200, 12/200))
        shape_designer.generate_circle(some_obstacle, diameter=12)
        self.assertTrue(np.array_equal(canvas, circle_6))

    def test_givenCanvas_whenGeneratingCircleOfRadius7_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(15)
        some_obstacle = Obstacle(WorldCoordinate(14/200, 14/200))
        shape_designer.generate_circle(some_obstacle, diameter=14)
        self.assertTrue(np.array_equal(canvas, circle_7))

    def test_givenCanvas_whenGeneratingCircleOfRadius6WithInflate_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(25)
        some_obstacle = Obstacle(WorldCoordinate(24/200, 24/200))
        shape_designer.generate_circle(some_obstacle, diameter=24)
        self.assertTrue(np.array_equal(canvas, circle_12))

    def test_givenCanvas_whenGeneratingCircleWithOffsetCenter_thenReturnCroppedCircle(self):
        canvas, shape_designer = self.create_canvas(13)
        some_obstacle = Obstacle(WorldCoordinate(6/100, 9/100))
        shape_designer.generate_circle(some_obstacle, diameter=12)
        self.assertTrue(np.array_equal(canvas, circle_6_cropped))

    def test_givenCanvas_whenGeneratingCube12x12_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(12)
        some_cube = Cube(WorldCoordinate(12/200, 12/200), CubeColour.RED)
        cube_storage = CubeStorage([some_cube])
        shape_designer.generate_cube(cube_storage, width=12)
        self.assertTrue(np.array_equal(canvas, cube_12))

    def test_givenCanvas_whenGeneratingBasicCube_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(12)
        some_cube = Cube(WorldCoordinate(12/200, 12/200), CubeColour.RED)
        cube_storage = CubeStorage([some_cube])
        shape_designer.generate_cube(cube_storage, width=5)
        self.assertTrue(np.array_equal(canvas, cube_5))

    def test_givenCanvas_whenGeneratingBasicCubeWithInflate_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(12)
        some_cube = Cube(WorldCoordinate(12/200, 12/200), CubeColour.RED)
        cube_storage = CubeStorage([some_cube])
        shape_designer.generate_cube(cube_storage, width=10)
        self.assertTrue(np.array_equal(canvas, cube_5_inflated))

    def test_givenCanvas_whenGeneratingBasicBorders_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(12)
        shape_designer.generate_borders(border=5)
        self.assertTrue(np.array_equal(canvas, basic_borders))

    def test_givenCanvas_whenGeneratingBasicBordersWithInflate_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(19)
        shape_designer.generate_borders(border=7)
        self.assertTrue(np.array_equal(canvas, inflated_borders))

    def test_givenCanvas_whenGeneratingCubeStorage_thenReturnGoodShape(self):
        canvas, shape_designer = self.create_canvas(12)
        shape_designer.generate_cube_storage(5, 8)
        self.assertTrue(np.array_equal(canvas, cube_storage))


